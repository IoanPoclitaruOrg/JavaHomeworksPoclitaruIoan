/*
 * Class Lesson2ex5_2_ConditionalOperators. Exemplify the operators.  
 */

/**
 * Examples with conditional operators. 
 * @author poclitar
 */

public class Lesson2ex5_2_ConditionalOperators {
    public static void main (String[] args){
    int a, b;
    a = 4;
    b = 6;
    System.out.println((a == b) ? "a = b, true" : "a = b, false");
    System.out.println((a < b) ? 4 : 6);
    
    }
}
