package numericalcalculator;

/**
 * The class subtracts the value of num2 from num1. It uses 2 parameters num1 
 * and num2, of type double, and returns a double as result of the operation. 
 * @author poclitar
 */
public class Minus { // class's name is Minus.
    public double subtract (double num1, double num2){ /* is a public class, 
        * it returns a value of type double, method's name is subtract (verb) 
        * and uses 2 parameters of type double, num1 and  num2 */
        double result = num1 - num2; // the result of the subtraction is a 
        // double type variable.
        return (result);
    }
    
}
