package numericalcalculator;

/**
 * A public class which multiplies 2 variables num1 and num2 of types double,
 * returning a double variable result.
 * @author poclitar
 */
public class Multiply {
    public double multiplication (double num1, double num2){ /* public method named 
    * multip, returning a double variable as result of multiply operation between 
    * 2 numbers, num and num2 of double type. */
        double result = num1 * num2; // calculate the result of the 
        // multiplication method and declare it as a variable of double type.
        return (result);
    }
}
